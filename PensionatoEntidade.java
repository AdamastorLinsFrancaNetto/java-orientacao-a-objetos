package entidade;

public class PensionatoEntidade {
	
	String nome;
	int ID;
	
	public PensionatoEntidade(String nome, int ID) {
		this.nome = nome;
		this.ID = ID;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getID() {
		return ID;
	}

	public void setID(int ID) {
		this.ID = ID;
	}
	
	
	
	

}
